import pandas as pd
from pathlib import Path
import argparse


def clean_parquet(dataset_path: Path, bad_indices_file: Path, output_path: Path):
    print(f"Loading bad indices from {bad_indices_file}...")
    with open(bad_indices_file, "r") as f:
        # Read the text file and convert lines to a set of integers
        bad_indices = set(int(line.strip()) for line in f if line.strip())

    if not bad_indices:
        print("No bad indices found. Nothing to do.")
        return

    print(f"Loading parquet dataset: {dataset_path} ...")
    df = pd.read_parquet(dataset_path, engine="pyarrow")

    original_size = len(df)

    print(f"Dropping {len(bad_indices)} rows...")
    # Drop rows by integer index
    df_clean = df.drop(index=list(bad_indices))

    # Save back to parquet
    print(f"Saving cleaned dataset to {output_path} ...")
    df_clean.to_parquet(output_path, engine="pyarrow", index=False)

    print(f"Done! Original size: {original_size} | New size: {len(df_clean)}")


if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("--dataset", type=Path, required=True, help="Original parquet file")
    ap.add_argument("--bad-indices", type=Path, required=True, help="Text file generated by the compiler script")
    ap.add_argument("--output", type=Path, required=True, help="Path for the new cleaned parquet file")
    args = ap.parse_args()

    clean_parquet(args.dataset, args.bad_indices, args.output)
